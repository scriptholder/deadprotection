export function generateAsciiArt(text: string): string {
  const upperText = text.toUpperCase().slice(0, 12); // Limit to 12 chars
  const lines: string[] = ['', '', '', '', '', ''];
  
  for (const char of upperText) {
    const charArt = ASCII_FONT[char] || ASCII_FONT[' '];
    for (let i = 0; i < 6; i++) {
      lines[i] += charArt[i];
    }
  }
  
  return lines.join('\n');
}

